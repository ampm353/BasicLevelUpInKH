package com.jin.variable;

public class Variable {
	// 변수란, 기본적으로 '변하는 수'를 의미하며,
	// 좀 더 컴퓨터의 관점에서 말하자면 '변하는 수'를 저장하는 '(메모리)공간'을 의미. 
	public static void main(String[] args) {
		int myAge = 26;
		// 앞서서 언급한 카멜 표기법을 이용하여 변수의 이름을 정해준다.
		
		System.out.println("저의 나이는 " + myAge + "살 입니다.");
		// 첫 날 배운 출력문을 통해서 특정문자열("저의 나이는 ", "살 입니다.")과 변수(myAge)를 출력.


	}

}
